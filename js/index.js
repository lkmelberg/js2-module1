/**
 * Adds two numbers together
 * @param {number} a First value
 * @param {number} b Second value
 * @returns {number} Sum of params
 * @example
 * ```js
 * // Add two numbers together
 * const a = 1;
 * const b = 2;
 * const sum = okay(a, b);
 * // expect sum to be 3
 * ```
 */
export function okay(a, b) {
  return a + b;
}
